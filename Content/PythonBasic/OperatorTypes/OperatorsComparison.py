# Operadores de Comparação em Python

# Declaração de variáveis
x = 10
y = 20

# Operadores de Comparação
igual = (x == y)             # Igualdade
diferente = (x != y)        # Diferença
maior = (x > y)             # Maior que
menor = (x < y)             # Menor que
maior_ou_igual = (x >= y)   # Maior ou igual a
menor_ou_igual = (x <= y)   # Menor ou igual a

# Exibindo os resultados
print(f"Valores: x = {x}, y = {y}")
print(f"Igualdade: {x} == {y} => {igual}")
print(f"Diferença: {x} != {y} => {diferente}")
print(f"Maior que: {x} > {y} => {maior}")
print(f"Menor que: {x} < {y} => {menor}")
print(f"Maior ou igual a: {x} >= {y} => {maior_ou_igual}")
print(f"Menor ou igual a: {x} <= {y} => {menor_ou_igual}")
