# Operadores de Atribuição em Python

# Atribuição simples
x = 10
print(f"Atribuição simples: x = {x}")

# Atribuição com operadores aritméticos
y = 5
x += y  # Equivalente a x = x + y
print(f"Atribuição com adição: x += {y} => x = {x}")

x -= y  # Equivalente a x = x - y
print(f"Atribuição com subtração: x -= {y} => x = {x}")

x *= y  # Equivalente a x = x * y
print(f"Atribuição com multiplicação: x *= {y} => x = {x}")

x /= y  # Equivalente a x = x / y
print(f"Atribuição com divisão: x /= {y} => x = {x}")

# Atribuição com operadores de módulo
x %= y  # Equivalente a x = x % y
print(f"Atribuição com módulo: x %= {y} => x = {x}")

# Atribuição com operadores de exponenciação
x **= 2  # Equivalente a x = x ** 2
print(f"Atribuição com exponenciação: x **= 2 => x = {x}")

# Atribuição com operadores de inteiro (divisão)
x //= 3  # Equivalente a x = x // 3
print(f"Atribuição com divisão inteira: x //= 3 => x = {x}")
