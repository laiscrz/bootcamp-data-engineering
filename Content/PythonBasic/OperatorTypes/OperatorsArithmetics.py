# Aritmética Básica em Python

# Declaração de variáveis
a = 10
b = 5

# Operadores Aritméticos
soma = a + b
subtracao = a - b
multiplicacao = a * b
divisao = a / b
divisao_inteira = a // b
modulo = a % b
exponenciacao = a ** b

# Exibindo os resultados
print(f"Valores: a = {a}, b = {b}")
print(f"Soma: {a} + {b} = {soma}")
print(f"Subtração: {a} - {b} = {subtracao}")
print(f"Multiplicação: {a} * {b} = {multiplicacao}")
print(f"Divisão: {a} / {b} = {divisao:.2f}")  # Formatação para duas casas decimais
print(f"Divisão Inteira: {a} // {b} = {divisao_inteira}")
print(f"Módulo: {a} % {b} = {modulo}")
print(f"Exponenciação: {a} ** {b} = {exponenciacao}")
